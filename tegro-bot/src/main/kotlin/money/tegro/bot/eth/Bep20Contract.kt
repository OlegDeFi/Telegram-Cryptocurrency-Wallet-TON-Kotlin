class Bep20Contract(
    contractAddress: String,
    web3j: Web3j,
    credentials: Credentials
) : Contract(
    contractBinary,
    contractAddress,
    web3j, credentials,
    DefaultGasProvider()
) {
    fun balanceOf(owner: Address): Uint256 {
        val function = Function(
            "balanceOf",
            listOf<Type<*>>(owner),
            listOf<TypeReference<*>>(object : TypeReference<Uint256>() {})
        )
        return executeCallSingleValueReturn(function)
    }

    fun transfer(to: Address, amount: Uint256): TransactionReceipt {
        val function = Function("transfer", listOf<Type<*>>(to, amount), emptyList())
        return executeTransaction(function)
    }
}